package com.example.apnaevent2;

public class Bookings {

    String Booking_UserId;
    String Booking_VendorId;
    String Booking_TotalAmount;
    String Booking_FromDate;
    String Booking_ToDate;

    public Bookings() {
    }


    public String getBooking_UserId() {
        return Booking_UserId;
    }

    public void setBooking_UserId(String booking_UserId) {
        Booking_UserId = booking_UserId;
    }

    public String getBooking_VendorId() {
        return Booking_VendorId;
    }

    public void setBooking_VendorId(String booking_VendorId) {
        Booking_VendorId = booking_VendorId;
    }

    public String getBooking_TotalAmount() {
        return Booking_TotalAmount;
    }

    public void setBooking_TotalAmount(String booking_TotalAmount) {
        Booking_TotalAmount = booking_TotalAmount;
    }

    public String getBooking_FromDate() {
        return Booking_FromDate;
    }

    public void setBooking_FromDate(String booking_FromDate) {
        Booking_FromDate = booking_FromDate;
    }

    public String getBooking_ToDate() {
        return Booking_ToDate;
    }

    public void setBooking_ToDate(String booking_ToDate) {
        Booking_ToDate = booking_ToDate;
    }
}
